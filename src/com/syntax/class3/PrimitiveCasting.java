package com.syntax.class3;

public class PrimitiveCasting {

	public static void main(String[] args) {

	//main+ctrl+space
		//ctrl+space-->autocomplete
		
		int i=100;
		//widening or implicit
		//we store ind value 10inside double variable
		double d=10;
		System.out.println(i);//10
		System.out.println(d);//10.0
		//int num=10.0;//Type mismatch:can not convert from double to int
		
		//narrowing or explicit casting
		//we store double value 1099 into int variable
		int num=(int)10.99;
		System.out.println(num);
		
		float f=10.99f; //Type mimatch can not convert from double to float
		
		
		
		
		
		
		
		
		
		
		
		
	}

	}


